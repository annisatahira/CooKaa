public class AllRecipe {

    private int imgAllRecipe;
    private String namaAllRecipe;

    public AllRecipe(int imgAllRecipe, String namaAllRecipe) {
        this.imgAllRecipe = imgAllRecipe;
        this.namaAllRecipe = namaAllRecipe;
    }

    public int getImgAllRecipe() {
        return imgAllRecipe;
    }

    public String getNamaAllRecipe() {
        return namaAllRecipe;
    }
}
